#include "Math.h"
#include <cmath>

double mat::sum(double num1, double num2) { return num1 + num2; }
double mat::difference(double num1, double num2) { return num1 - num2; }
double mat::multiplication(double num1, double num2) { return num1 * num2; }
double mat::division(double num1, double num2) { return num1 / num2; }
double mat::exponent(double num1, double num2) { return pow(num1,num2); }